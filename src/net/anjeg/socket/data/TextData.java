package net.anjeg.socket.data;

import net.anjeg.socket.Data;

public class TextData implements Data<String> {

	/* ************************************************************************
	 * Constants
	 * ***********************************************************************/

	/* ************************************************************************
	 * Attributes
	 * ***********************************************************************/

	private String text;

	/* ************************************************************************
	 * Constructor
	 * ***********************************************************************/

	public TextData(String text) {
		super();
		
		this.text = text;
	}
	
	/* ************************************************************************
	 * Methods
	 * ***********************************************************************/

	/* ************************************************************************
	 * Overrides
	 * ***********************************************************************/

	@Override
	public String getName() {

		return "TEXT";
	}

	@Override
	public byte[] toBytes() {
		
		return this.text.getBytes();
	}

	@Override
	public String getData() {
		
		return this.text;
	}

}
